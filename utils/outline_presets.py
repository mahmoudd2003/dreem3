# قوالب Outline جاهزة بصيغة Markdown (H2/H3)

def outline_modern() -> str:
    return """## خلاصة سريعة
## لماذا قد يظهر هذا الرمز؟
## متى لا ينطبق التفسير؟
## سيناريوهات واقعية
## أسئلة شائعة
## مقارنة دقيقة
## منهجية التفسير
## مصادر صريحة
## خاتمة مسؤولة + تنويه""".strip()

def outline_classic() -> str:
    return """## خلاصة سريعة
## لماذا قد يظهر هذا الرمز؟
## متى لا ينطبق التفسير؟
## سيناريوهات واقعية
## أسئلة شائعة
## تفسير الرمز حسب حالة الرائي
### تفسير الرمز للعزباء
### تفسير الرمز للمتزوجة
### تفسير الرمز للحامل
### تفسير الرمز للمطلقة
### تفسير الرمز للرجل
## مقارنة دقيقة
## منهجية التفسير
## مصادر صريحة
## خاتمة مسؤولة + تنويه""".strip()

def get_outline(mode: str) -> str:
    """
    mode: "modern" | "classic" | "none"
    """
    mode = (mode or "").strip().lower()
    if mode == "classic":
        return outline_classic()
    if mode == "none":
        return ""
    # الافتراضي
    return outline_modern()
