# قوالب Outline جاهزة بصيغة Markdown (H2/H3)

def outline_modern() -> str:
    """
    Modern Outline (الإصدار المعتمد):
    - يضم "حسب حالة الرائي" (مختصر) و"أقوال المفسرين" المكمل
    - يحافظ على أقسام Helpful Content + E-E-A-T
    """
    return """## خلاصة سريعة
## لماذا قد يظهر هذا الرمز؟
## متى لا ينطبق التفسير؟
## سيناريوهات واقعية
## تفسير حسب حالة الرائي (مختصر ومنظم)
### للعزباء
### للمتزوجة
### للحامل
### للمطلقة
### للرجل
## أقوال المفسرين (ابن سيرين / النابلسي / ابن شاهين)
## أسئلة شائعة
## مقارنة دقيقة
## منهجية التفسير
## مصادر صريحة
## خاتمة مسؤولة + تنويه مهني""".strip()


def outline_classic() -> str:
    """
    قالب كلاسيكي (للاستخدام عند الحاجة). يمكن إبقاؤه كخيار ثانٍ.
    """
    return """## خلاصة سريعة
## لماذا قد يظهر هذا الرمز؟
## متى لا ينطبق التفسير؟
## سيناريوهات واقعية
## أسئلة شائعة
## تفسير الرمز حسب حالة الرائي
### تفسير الرمز للعزباء
### تفسير الرمز للمتزوجة
### تفسير الرمز للحامل
### تفسير الرمز للمطلقة
### تفسير الرمز للرجل
## مقارنة دقيقة
## منهجية التفسير
## مصادر صريحة
## خاتمة مسؤولة + تنويه""".strip()


def get_outline(mode: str) -> str:
    """
    mode: "modern" | "classic" | "none"
    """
    mode = (mode or "").strip().lower()
    if mode == "classic":
        return outline_classic()
    if mode == "none":
        return ""
    # الافتراضي
    return outline_modern()
