# تعليقات محرّر جاهزة لإضافة بصمة بشرية قصيرة ومتغيّرة
# - تُستخدم لإدراج قسم "تعليق المحرّر" تلقائيًا
# - ترجع "نص الفقرة" فقط (بدون عنوان H2)
