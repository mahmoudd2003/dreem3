from __future__ import annotations
import random

NOTES = [
    "لاحظنا أن هذا النوع من الرؤى يتكرر غالبًا مع ضغوط حياتية يومية، وقد يعكس التوتر أكثر من كونه رسالة غيبية.",
    "من المفيد فحص مشاعرك أثناء الرؤيا وبعدها؛ كثيرًا ما تفسّر المشاعر معنى الرمز أكثر من الرمز نفسه.",
    "تكرار الرمز في فترات الامتحانات أو التغيّر المهني شائع بين القرّاء؛ لذا ننصح بقراءة الحلم ضمن سياقه الراهن.",
    "أحيانًا يكون الحلم انعكاسًا لحدث قريب أو نقاش محتدم قبل النوم؛ هذا لا يُبطله لكنه يخفّف من قطعيّته.",
    "حين تختلط تفاصيل الرؤيا مع قلق يومي واضح، نميل لتقديم تفسير احتمالي محافظ بدل معنى واحد ثابت.",
]

def get_editor_note_body() -> str:
    """
    يعيد نصًا قصيرًا لتعلـيق المحرّر (بدون عنوان H2).
    """
    return random.choice(NOTES)
